'use client';
import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { useState, useEffect } from 'react';

export default function Sidebar() {
  const pathname = usePathname();
  const [isOpen, setIsOpen] = useState(false);
  const [isCollapsed, setIsCollapsed] = useState(false);

  useEffect(() => {
    const handleCollapse = (e) => setIsCollapsed(e.detail);
    window.addEventListener('set-sidebar-collapse', handleCollapse);
    return () => window.removeEventListener('set-sidebar-collapse', handleCollapse);
  }, []);

  const navItems = [
    { label: 'Dashboard', path: '/admin/dashboard', icon: <DashboardIcon /> },
    { label: 'Produk', path: '/admin/products', icon: <ProductIcon /> },
    { label: 'Kasir POS', path: '/admin/pos', icon: <PosIcon /> },
    { label: 'Pesanan', path: '/admin/orders', icon: <OrderIcon /> },
    { label: 'Transaksi', path: '/admin/transactions', icon: <TransactionIcon /> },
    { label: 'Laporan', path: '/admin/reports', icon: <ReportIcon /> },
    { label: 'Pelanggan', path: '/admin/customers', icon: <CustomerIcon /> },
  ];

  // Removed bottomItems for settings and logout as per request

  return (
    <>
      <button 
        className="mobile-menu-btn" 
        onClick={() => setIsOpen(!isOpen)}
        style={{ position: 'fixed', top: '16px', left: '16px', zIndex: 100, padding: '8px', background: 'white', borderRadius: '8px', border: '1px solid #e2e8f0', display: 'none' }}
      >
        <svg width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="#1e293b" strokeWidth="2"><line x1="3" y1="12" x2="21" y2="12"/><line x1="3" y1="6" x2="21" y2="6"/><line x1="3" y1="18" x2="21" y2="18"/></svg>
      </button>

      {isOpen && (
        <div 
          className="mobile-overlay" 
          onClick={() => setIsOpen(false)} 
          style={{ position: 'fixed', inset: 0, backgroundColor: 'rgba(0,0,0,0.5)', zIndex: 40 }}
        />
      )}

      <aside className={`sidebar ${isOpen ? 'mobile-open' : ''} ${isCollapsed ? 'collapsed' : ''}`}>
        <button 
          className="sidebar-collapse-btn"
          onClick={() => setIsCollapsed(!isCollapsed)}
        >
          {isCollapsed ? <ChevronRightIcon /> : <ChevronLeftIcon />}
        </button>

        <div className="sidebar-profile-top">
          <div className="user-avatar-large">
            <img src="https://i.pravatar.cc/150?img=11" alt="UD Kusuma" />
          </div>
          <div className="sidebar-profile-info">
            <div className="user-name-large">UD Kusuma</div>
            <div className="user-role-large">Admin / Manager</div>
          </div>
        </div>

        <ul className="sidebar-menu">
          {navItems.map((item) => {
            const isActive = pathname === item.path;
            return (
              <li key={item.path}>
                <Link href={item.path} className={`sidebar-item ${isActive ? 'active' : ''}`}>
                  {item.icon}
                  <span className="sidebar-item-label">{item.label}</span>
                </Link>
              </li>
            );
          })}
        </ul>

        {/* Optional decorative bottom or active users if needed later */}
        <div style={{ flex: 1 }}></div>
      </aside>
    </>
  );
}

